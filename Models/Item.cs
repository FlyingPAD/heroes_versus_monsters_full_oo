namespace Models
{
    public class Item
    {
        // ----------------------------------------------------------------------------------------------------------------------
        // VARIABLES / PROPERTIES
        // ----------------------------------------------------------------------------------------------------------------------
        public string? ItemName { get; set; }
        public int ItemQuantity { get; set; }
        // ----------------------------------------------------------------------------------------------------------------------
        // CONSTRUCTOR
        // ----------------------------------------------------------------------------------------------------------------------
        public Item(string? item_name, int item_quantity)
        {
            ItemName = item_name;
            ItemQuantity = item_quantity;
        }
    }
}